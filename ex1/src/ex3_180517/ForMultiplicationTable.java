package ex3_180517;

public class ForMultiplicationTable {

	public static void main(String[] args) {
		
		for(int i=1; i<10; i++) {
			System.out.print(i+"단 출력: ");
			for(int a=1; a<10; a++) {
				System.out.print(i+"*"+a+"="+i*a+" ");
			}
			System.out.println();}
		
		

	}

}
